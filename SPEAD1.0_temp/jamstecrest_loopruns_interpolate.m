function [AI] = jamstecrest_loopruns_interpolate(A)

%====================================================================
%....................................................................
[mrows,ncols] = size(A); 
%....................................................................
nsize = lcm(mrows,ncols);
%....................................................................
y = [1:mrows];
x = [1:ncols];
%....................................................................
dy = (mrows-1) / (nsize-1);
dx = (ncols-1) / (nsize-1);
%....................................................................
yi = [1:dy:mrows];
xi = [1:dx:ncols];
%....................................................................
[X,Y] = meshgrid(x,y); 
%....................................................................
[XI,YI] = meshgrid(xi,yi); 
%....................................................................
[AI] = interp2(X,Y,A,XI,YI); 
%....................................................................
%====================================================================


%********************************************************************
return

