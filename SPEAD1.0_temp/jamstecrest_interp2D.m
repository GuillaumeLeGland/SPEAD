function [AI] = jamstecrest_interp2D(A,ndays)

%========================================================================
%........................................................................
zmin = 1;
zmax = 200;
deltaz = 10;
%........................................................................
zdepths = [deltaz:deltaz:zmax]; %all nodes.
ndepths = length(zdepths); %number of vertical nodes.
%........................................................................
dz = 1; %[m]
xtimes = [1:ndays]; %[days] 
%........................................................................
zdepths1 = [0:deltaz:zmax];
zdepths2 = [0:dz:zmax];
%........................................................................
zend = 200;
J = find(zdepths2 <= zend);
jend = J(end); 
dj = 50/dz; 
%........................................................................
[X1,Z1] = meshgrid(xtimes,zdepths1);
[X2,Z2] = meshgrid(xtimes,zdepths2);
%........................................................................
Aj = [A;A(end,:)]; %add extra data at depth to have 201 vertical points.
Aj = interp2(X1,Z1,Aj,X2,Z2); 
%%AI = Aj(1:end-1,:); %[200m]
AI = Aj; %[201m]
%........................................................................
%========================================================================
%************************************************************************
return

