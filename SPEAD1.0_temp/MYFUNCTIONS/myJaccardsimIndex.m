function [JSIsim,N00,N11,N01,N10] = myJaccardsimIndex(A,B)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%MAKE DATA AS PRESENCE/ABSENCE ONLY:
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%===================================================================
%...................................................................
Azerones = zeros(1,length(A));
Bzerones = zeros(1,length(B));
%...................................................................
Iones = find(A > 0);
Jones = find(B > 0);
%...................................................................
Azerones(Iones) = 1.0;
Bzerones(Jones) = 1.0;
%...................................................................
A = Azerones; 
B = Bzerones; 
%...................................................................
%===================================================================

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%COMPUTE JACCARD SIMILARITY INDEX:
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%===================================================================
%-------------------------------------------------------------------
%A: sample-1
%B: sample-2
%-------------------------------------------------------------------
%JSI = N11 / (N11 + N10 + N01) 
%-------------------------------------------------------------------
%N00: represents the total number of attributes where A and B both have a value of 0.
%N11: represents the total number of attributes where A and B both have a value of 1.
%N10: represents the total number of attributes where the attribute of A is 1 and the attribute of B is 0.
%N01: represents the total number of attributes where the attribute of A is 0 and the attribute of B is 1.
%-------------------------------------------------------------------
%...................................................................
N00 = length(find(A==0 & B==0)); %Void. 
N11 = length(find(A==1 & B==1)); %Persistence. 
N10 = length(find(A==1 & B==0)); %Invasion. 
N01 = length(find(A==0 & B==1)); %Extinction. 
%...................................................................
JSIsim = N11 / (N11 + N10 + N01);
%...................................................................
%===================================================================
%*******************************************************************
return
