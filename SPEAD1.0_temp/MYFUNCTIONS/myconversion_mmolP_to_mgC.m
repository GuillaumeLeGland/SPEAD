function [FP_mgC] = myconversion_mmolP_to_mgC(FP_mmolP)

%========================================================================
%........................................................................
%NOTE: Conversion = 1 (mmolP * m-3) x 106 (mmolC * m-3) x (12.00 / 1.00) * (mgC * mmolC-1) 
%........................................................................
fc_mmolP_to_mgC = 106 * 12.00;
%........................................................................
FP_mgC = FP_mmolP * fc_mmolP_to_mgC;
%........................................................................
%========================================================================
%************************************************************************
return

