function [BCIsim] = myBrayCurtisIndex(A,B)

%===========================================================
%...........................................................
Inan = find(isnan(A)==1);
Jnan = find(isnan(B)==1);
%...........................................................
A(Inan) = 0;
B(Jnan) = 0;
%...........................................................
%===========================================================
%...........................................................
sumA = sum(A); %total number of cells in sample-1
sumB = sum(B); %total number of cells in sample-2
%...........................................................
diffAB = abs(A - B); %number of cells in sample-1 minus number cells in sample-2
%...........................................................
BCIdis = sum(diffAB) / (sumA + sumB);
%...........................................................
BCIsim = 1d0 - BCIdis;
%...........................................................
%===========================================================
%***********************************************************
return



