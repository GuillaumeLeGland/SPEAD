function [ticksRound] = mylogroundticks(ticks)

%========================================================================
%........................................................................
ticks = ticks(:)'; %make sure is a row vector. 
%........................................................................
dprnd = @(x, dps)round(x .* (10.^dps))./(10.^dps); %Function for rounding to specified decimal places.
%........................................................................
[~,ticks_expo] = mymantexpnt(ticks); %exponent of the numbers using scientific notation. 
%........................................................................
dps = abs(ticks_expo); %decimal places to be rounded. 
%........................................................................
ticksRound = dprnd(ticks,dps);
%........................................................................
%========================================================================

return
