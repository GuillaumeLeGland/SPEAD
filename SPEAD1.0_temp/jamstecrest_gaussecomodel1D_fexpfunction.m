function [yexp,fexp,fexpesc] = jamstecrest_gaussecomodel1D_fexpfunction(ndepths,keyScaling)

%========================================================================
%........................................................................
xmin =   0;
xmax =  10;
xdel = (xmax - xmin) / (ndepths - 1);
xrng = [xmin:xdel:xmax];
%........................................................................
if strcmp(keyScaling,'linear')
    fexpesc = (1 - xrng/xmax); 
elseif strcmp(keyScaling,'logarithmic')
    %....................................................................
    y0 = 1.0; 
    Base = 2.0;
    %%Beta = 1.0;
    Beta = 0.35;
    %....................................................................
    yexp = y0*Base.^(-Beta*xrng)';
    %....................................................................
    fexp = (yexp - min(yexp)); 
    %....................................................................
    fexpmin = 0; 
    fexpmax = 1; 
    fexpesc = fexpmax - (fexpmax-fexpmin)*((max(fexp)-fexp)./(max(fexp)-fexpmin)); %Enforce fmax y fmin values scaling. 
    %....................................................................
end
%........................................................................
%========================================================================
return
